package mmc;

public class Probabilidad {
	private Double pn;
	private Double pnt;
	public Probabilidad(Double pn, Double pnt) {
		this.pn = pn;
		this.pnt = pnt;
	}
	public Double getPn() {
		return pn;
	}
	public void setPn(Double pn) {
		this.pn = pn;
	}
	public Double getPnt() {
		return pnt;
	}
	public void setPnt(Double pnt) {
		this.pnt = pnt;
	}
	
}
